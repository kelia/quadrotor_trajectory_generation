import numpy as np


class Quad:
    def __init__(self, config):
        self.mass = config.quad_mass
        self.max_thrust_per_motor = config.quad_max_mot_thrust

        self.J = np.array(config.quad_inertia)
        h = np.cos(np.pi / 4) * config.quad_arm_length

        # TODO: parameterize this as well
        self.x_f = np.array([h, -h, -h, h])
        self.y_f = np.array([-h, -h, h, h])

        # For z thrust torque calculation
        self.c = config.rotor_drag_coeff  # m   (z torque generated by each motor)
        self.z_l_tau = np.array([-self.c, self.c, -self.c, self.c])
